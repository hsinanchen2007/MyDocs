#include <iostream>
using namespace std;

int main()
{
   int num1 = 10;
   int num2 = 5;

   cout << "对 num1 和 num2 进行各种运算。\n";
   cout << "num1+num2的结果是" << num1+num2 << "这个值。\n";
   cout << "num1-num2的结果是" << num1-num2 << "这个值。\n";
   cout << "num1*num2的结果是" << num1*num2 << "这个值。\n";
   cout << "num1/num2的结果是" << num1/num2 << "这个值。\n";
   cout << "num1%num2的结果是" << num1%num2 << "这个值。\n";

   return 0;
}
