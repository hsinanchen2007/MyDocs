#include <iostream>
using namespace std;

int main(int argc, char** argv)
{
	int a=87, b=93, c=85;	//定義與起始變數

	cout << "a = " << a << endl;	//輸出變數值
	cout << "b = " << b << endl;	//輸出變數值
	cout << "c = " << c << endl;	//輸出變數值
	int sum = a + b + c;	//計算總和
	cout << "總和 = " << sum << endl;	//輸出總和
	return 0;
}
